		<script>
			$(document).ready(function(){
				store();
				list();
				update();

				$("#collapse_Configuraciones").addClass("show");
				$("#nav_li_Configuraciones").addClass("open");
				$("#nav_users, #modulo_Configuraciones").addClass("active");
